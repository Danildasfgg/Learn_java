public class Learn_day_fiveteen {
    static int add(int oneNumber, int twoNumber){
        return 1 + oneNumber + twoNumber;
    }
    static int subtract(int oneNumber, int twoNumber){
        return oneNumber - twoNumber;
    }
    public static void main(String[] args){
        System.out.println(add(5, 4));
        System.out.println(subtract(9, 6));
    }
}
